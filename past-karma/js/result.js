function initResult() {
  const path = window.location.pathname;
  const match = path.match(/result-(\d+)\.html/);
  const id = match ? match[1] : '01';
  const data = RESULTS[id];
  if (!data) return;

  const params = new URLSearchParams(window.location.search);
  const hasParams = params.has('b');
  const b = hasParams ? (parseInt(params.get('b')) || 0) : data.defaultScores.b;
  const h = hasParams ? (parseInt(params.get('h')) || 0) : data.defaultScores.h;
  const y = hasParams ? (parseInt(params.get('y')) || 0) : data.defaultScores.y;
  const g = hasParams ? (parseInt(params.get('g')) || 0) : data.defaultScores.g;
  const p = hasParams ? (parseInt(params.get('p')) || 0) : (data.defaultScores.p || 0);
  const total = b + h + y + g + p || 12;

  // Render main card
  const mainCard = document.getElementById('result-main-card');
  mainCard.style.background = data.gradient;

  document.getElementById('result-icon').textContent = data.icon;
  document.getElementById('result-name').textContent = data.name;
  document.getElementById('result-name-cn').textContent = data.nameCn;
  document.getElementById('result-tagline').textContent = data.tagline;

  // Render scores
  setTimeout(() => {
    document.getElementById('bar-bok').style.width = ((b / total) * 100) + '%';
    document.getElementById('bar-hye').style.width = ((h / total) * 100) + '%';
    document.getElementById('bar-yeon').style.width = ((y / total) * 100) + '%';
    document.getElementById('bar-go').style.width = ((g / total) * 100) + '%';
    const barPos = document.getElementById('bar-pos');
    if (barPos) barPos.style.width = ((p / total) * 100) + '%';
  }, 300);

  document.getElementById('val-bok').textContent = b + '점';
  document.getElementById('val-hye').textContent = h + '점';
  document.getElementById('val-yeon').textContent = y + '점';
  document.getElementById('val-go').textContent = g + '점';
  const valPos = document.getElementById('val-pos');
  if (valPos) valPos.textContent = p + '점';

  // 정화 메시지
  const purifyMsg = document.getElementById('purify-message');
  if (purifyMsg) {
    if (p >= 8) {
      purifyMsg.textContent = '🌟 영혼의 정화가 매우 높습니다. 전생의 업보가 거의 소진되어 가고 있어요.';
    } else if (p >= 5) {
      purifyMsg.textContent = '✨ 정화의 빛이 자라고 있습니다. 수행을 이어가면 업보가 맑아질 거예요.';
    } else if (p >= 2) {
      purifyMsg.textContent = '🌱 정화의 씨앗이 싹트고 있어요. 조금씩 마음을 돌보아 주세요.';
    } else {
      purifyMsg.textContent = '💭 업보 정화 수행이 큰 도움이 될 거예요. 첫걸음을 내딛어 보세요.';
    }
  }

  // Render description
  document.getElementById('result-desc').innerHTML =
    data.description +
    '<br><br><strong>🔮 전생 이야기</strong><br>' + data.pastLife +
    '<br><br><strong>🌏 이번 생의 과보</strong><br>' + data.currentLife;

  // Render practices
  const listEl = document.getElementById('practice-list');
  data.practices.forEach(p => {
    const li = document.createElement('li');
    li.textContent = p;
    listEl.appendChild(li);
  });
}

